private void crear(){
    String archivo = jtxtCi.getText()+".txt";
    File crea_ubicacion = new File(ubicacion);
    File crea_archivo = new File(ubicacion+archivo);
    if(jtxtCi.getText().equals("")){
        JOptionPane.showMessageDialog(rootPane,"No hay CI");
    }else{
        try {
            if(crea_archivo.exists()){
            JOptionPane.showMessageDialog(rootPane,"El registro ya existe");
        }else{
            crea_ubicacion.mkdirs();
            Formatter crea= new Formatter(ubicacion+archivo);
            crea.format("%s\r\n%s\r\n%s\r\n%s",
                    "Ci="+jtxtCi.getText(),
                    "Nombre="+jtxtNombre.getText(),
                    "Pais="+jtxtPais.getText(),
                    "Edad="+jtxtEdad.getText());
            crea.close();
            JOptionPane.showMessageDialog(rootPane,"Archivo creado");
            System.exit( 0 );
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,"Archivo no creado");
        }
    }
}
